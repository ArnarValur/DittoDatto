/// 4px-base spacing grid for consistent layout rhythm.
abstract final class DittoSpacing {
  /// 4px — extra small gaps, dense UI.
  static const double xs = 4;

  /// 8px — small gaps, icon padding.
  static const double sm = 8;

  /// 12px — compact gaps.
  static const double compact = 12;

  /// 16px — medium gaps, base unit.
  static const double md = 16;

  /// 16px — base unit, default padding (deprecated: use md).
  static const double base = 16;

  /// 24px — large gaps, section spacing.
  static const double lg = 24;

  /// 32px — extra large gaps, page margins.
  static const double xl = 32;
}
