/**
 * Get icon by value
 */
export function getIconByValue(value: string): IconItem | undefined {
  return materialIcons.find(icon => icon.value === value)
}

/**
 * Filter icons by search query
 */
export function filterIcons(query: string): IconItem[] {
  if (!query) return materialIcons
  const lowerQuery = query.toLowerCase()
  return materialIcons.filter(icon => 
    icon.label.toLowerCase().includes(lowerQuery)
  )
}
